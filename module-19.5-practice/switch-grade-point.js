// switch use grade define

var yourNumber = 80;
switch (yourNumber) {
     case (yourNumber >= 80 && yourNumber <= 100):
          console.log("your result is:A+")
          break;
     case (yourNumber >= 50 && yourNumber < 80):
          console.log("your result is:B")
          break;
     default:
          console.log("your are not passed")

}